package GeeksForGeeks__Java.T4_Methods;

public class Q1_BuiltIn_Function {
    public static void main(String[] args) {

        System.out.println(Math.sqrt(9));
        System.out.println(Math.cbrt(64));
        System.out.println(Math.floor(2.96));   // [GIF] Function
        System.out.println(Math.ceil(4.01));    // [GIF] + 1 Function
        System.out.println(Math.min(4,9));
        System.out.println(Math.max(4,91));     // Only two number can be compared in both min and max function.
        System.out.println(Math.pow(2,10));     // (negative real no. , must be an integer)

    }
}
